<?php


namespace App\Document\Traits;


trait IdentifiableTrait
{
    /**
     * @MongoDB\Id
     */
    protected $id;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id): void
    {
        $this->id = $id;
    }
}